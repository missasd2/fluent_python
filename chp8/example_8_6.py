"""
示例8-6 浅复制
"""

l1 = [3, [66, 55, 44], (7, 8, 9)]
l2 = list(l1) # l2是l1的浅浅复制
l1.append(100) # 把100追加到l1中对l2没有影响
l1[1].remove(55) # 由于l1[1]是对可变对象即列表的引用，所有l2[1]绑定的列表与l2[1]绑定的列表是同一个
print("l1: ", l1)
print("l2: ", l2)
l2[1] += [33, 22] # 同样对两者都有影响
l2[2] += (10, 11) # 对元组来说，+=运算符创建一个新元组，然后重新绑定给便利;此时l2和l2相同位置上的元素不是同一个对象
print("l1: ", l1)
print("l2: ", l2)